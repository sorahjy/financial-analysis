def load_etf_pool() -> List[Dict[str, Any]]:
    """校验并读取配置，保持顺序且稳定去重。"""
    rows: List[Dict[str, Any]] = []
    seen = set()
    for raw in fund_index:
        code = str(raw).strip()
        if not (len(code) == 6 and code.isdigit()):
            raise ValueError(f"ETF 配置代码必须是 6 位数字: {raw!r}")
        if code in seen:
            continue
        seen.add(code)
        name, category = _ETF_METADATA.get(code, (f"ETF {code}", "ETF"))
        rows.append({
            "code": code,
            "name": name,
            "category": category,
        })
    return rows


def etf_codes() -> set[str]:
    return {row["code"] for row in load_etf_pool()}
